def sumar(num1, num2):

    print("Aqui se realizara la suma de los numeros")
    print("Despues se regresaran los numero para su correspondiente procesamiento")
    return num1 + num2

print(f"Esta es la suma de 10 y 15: " + str(sumar(10,15)))


def resta(num1, num2):
    print("Aqui se realizara la resta de los numeros")
    print("Despues se regresaran los numeros para ver que hacemos con ellos")
    return num1 - num2

print(f"Esta es la resta de 25 menos 15: " + str(resta(25,15)))

def multiplicacion(num1, num2):
    print("Aqui se realizara la multiplicacion")
    return num1 * num2

print(f"La resta de los numeros es: "+ str(multiplicacion(5,8)))


